def post_batch(
    base_url: str,
    database_id: int,
    queries: list[str],
    timeout: int,
    *,
    case_id: int,
    difficulty: str,
    trace_run: str,
) -> tuple[int, dict[str, Any], float]:
    endpoint = (
        f"{base_url.rstrip('/')}/database/"
        f"{database_id}/check_containment_batch"
    )
    request = urllib.request.Request(
        endpoint,
        data=json.dumps({"queries": queries}).encode("utf-8"),
        method="POST",
        headers={
            "Content-Type": "application/json",
            "Accept": "application/json",
            "X-SpiderSQL-Test-ID": str(case_id),
            "X-SpiderSQL-Test-Category": "containment_unclassified",
            "X-SpiderSQL-Test-Difficulty": difficulty,
            "X-SpiderSQL-Trace-Run": trace_run,
        },
    )

    started = time.perf_counter()
    try:
        with urllib.request.urlopen(request, timeout=timeout) as response:
            status_code = response.getcode()
            raw = response.read().decode("utf-8", errors="replace")
    except urllib.error.HTTPError as exc:
        status_code = exc.code
        raw = exc.read().decode("utf-8", errors="replace")
    except Exception as exc:
        return (
            0,
            {"success": False, "_client_error": str(exc)},
            time.perf_counter() - started,
        )

    elapsed = time.perf_counter() - started
    try:
        payload = json.loads(raw) if raw.strip() else {}
    except json.JSONDecodeError:
        payload = {"success": False, "_raw_response": raw}

    if not isinstance(payload, dict):
        payload = {"success": False, "_raw_response": payload}

    return status_code, payload, elapsed


def extract_sql(result: dict[str, Any]) -> str:
    for key in ("sql", "final_sql", "query_sql"):
        value = result.get(key)
        if isinstance(value, str) and value.strip():
            return value.strip()

    generated = result.get("generated_sql")
    if isinstance(generated, str) and generated.strip():
        return generated.strip()
    if isinstance(generated, dict):
        for key in ("sql", "query", "generated_sql"):
            value = generated.get(key)
            if isinstance(value, str) and value.strip():
                return value.strip()

    execution = result.get("execution")
    if isinstance(execution, dict):
        for key in ("sql", "executed_sql"):
            value = execution.get(key)
            if isinstance(value, str) and value.strip():
                return value.strip()

    return "<NO SQL GENERATED>"


def first_present(mapping: dict[str, Any], *keys: str) -> Any:
    for key in keys:
        if key in mapping:
            return mapping.get(key)
    return None


def query_label(index: Any, fallback: int | None = None) -> str:
    if index is None:
        return f"Q{fallback}" if fallback is not None else "Q?"
    try:
        # query_id from the containment payload is already 1-based, so the
        # display label must NOT add another offset (fixes Q(n+1) off-by-one).
        return f"Q{int(index)}"
    except (TypeError, ValueError):
        return str(index)


def format_case(
    case: dict[str, Any],
    status_code: int,
    payload: dict[str, Any],
    elapsed: float,
    trace_run: str,
) -> str:
    lines: list[str] = [
        "=" * 120,
        (
            f"CASE {case['id']:03d} | DB{case['database_id']} "
            f"{case['database_name']} | {case['difficulty']} | {case['name']}"
        ),
        "=" * 120,
        f"TRACE RUN: {trace_run}",
        f"HTTP STATUS: {status_code}",
        f"ENDPOINT SUCCESS: {payload.get('success')}",
        f"ELAPSED SECONDS: {elapsed:.2f}",
        "",
        "NATURAL-LANGUAGE QUERIES",
    ]

    for number, query in enumerate(case["queries"], start=1):
        lines.append(f"Q{number}: {query}")

    lines.extend(["", "DESIGNED LOGICAL RELATIONSHIPS"])
    for expected in case["expected"]:
        lines.append(f"- {expected}")
    lines.append(
        "- Equivalent-on-current-database is acceptable for a designed "
        "containment edge when the current rows make both sets equal."
    )

    lines.extend(["", "GENERATED SQL / QUERY RESULTS"])
    query_results = payload.get("query_results") or []
    if not query_results:
        lines.append("<NO QUERY RESULTS RETURNED>")
    else:
        for fallback, result in enumerate(query_results, start=1):
            if not isinstance(result, dict):
                lines.append(f"Q{fallback}: <INVALID QUERY RESULT> {result!r}")
                continue

            query_id = first_present(result, "query_id", "query_index", "index")
            label = query_label(query_id, fallback)
            lines.append(
                f"{label}: success={result.get('success')} "
                f"safe={result.get('safe')} "
                f"rows={first_present(result, 'row_count', 'rows')} "
                f"source={result.get('selected_candidate_source')} "
                f"score={result.get('selected_candidate_score')}"
            )
            lines.append(extract_sql(result))
            if result.get("warnings"):
                lines.append(f"WARNINGS: {result.get('warnings')}")

            error = first_present(
                result,
                "error",
                "execution_error",
                "endpoint_error",
            )
            if error:
                lines.append(f"ERROR: {error}")

    lines.extend(["", "CONTAINMENT SUMMARY"])
    summaries = payload.get("query_summaries") or []
    if not summaries:
        lines.append("<NO QUERY SUMMARIES RETURNED>")
    else:
        for fallback, summary in enumerate(summaries, start=1):
            if not isinstance(summary, dict):
                lines.append(f"Q{fallback}: <INVALID SUMMARY> {summary!r}")
                continue

            query_id = first_present(
                summary,
                "query_index",
                "query_id",
                "index",
            )
            lines.append(
                f"{query_label(query_id, fallback)}: "
                f"status={summary.get('status')} | "
                f"contained_in={summary.get('contained_in') or '-'} | "
                f"contains={summary.get('contains') or '-'} | "
                f"equivalent_to={summary.get('equivalent_to') or '-'} | "
                f"incomparable_with={summary.get('incomparable_with') or '-'} | "
                f"unknown_with={summary.get('unknown_with') or '-'} | "
                f"empty_result={summary.get('empty_result')}"
            )

    lines.extend(["", "PAIRWISE CONTAINMENT RESULTS"])
    pairs = payload.get("pairwise_relationships") or []
    if not pairs:
        lines.append("<NO PAIRWISE RESULTS RETURNED>")
    else:
        for pair in pairs:
            if not isinstance(pair, dict):
                lines.append(f"<INVALID PAIR RESULT> {pair!r}")
                continue

            index_a = first_present(pair, "query_a_index", "query_a")
            index_b = first_present(pair, "query_b_index", "query_b")
            label_a = query_label(index_a)
            label_b = query_label(index_b)
            lines.append(
                f"{label_a} vs {label_b}: {pair.get('relationship')}"
            )

            if pair.get("compared_on"):
                lines.append(f"  compared_on: {pair.get('compared_on')}")
            if pair.get("explanation"):
                lines.append(f"  explanation: {pair.get('explanation')}")

            a_minus_b = pair.get("a_minus_b_rows") or []
            b_minus_a = pair.get("b_minus_a_rows") or []
            if a_minus_b:
                lines.append(
                    f"  {label_a} minus {label_b}: "
                    f"{len(a_minus_b)} row(s); sample={a_minus_b[:5]}"
                )
            if b_minus_a:
                lines.append(
                    f"  {label_b} minus {label_a}: "
                    f"{len(b_minus_a)} row(s); sample={b_minus_a[:5]}"
                )

    lines.extend(
        [
            "",
            "TOP-LEVEL CONTAINMENT METADATA",
            (
                f"proof_type={payload.get('proof_type')} | "
                f"checked_on_current_database="
                f"{payload.get('checked_on_current_database')}"
            ),
        ]
    )
    if payload.get("limitations"):
        lines.append(f"limitations={payload.get('limitations')}")
    if payload.get("warnings"):
        lines.append(f"warnings={payload.get('warnings')}")

    lines.extend(
        [
            "",
            "RAW RESPONSE JSON",
            json.dumps(payload, indent=2, ensure_ascii=False),
            "",
        ]
    )
    return "\n".join(lines)


def main() -> int:
    args = parse_args()

    if not 1 <= args.start_case_id <= args.end_case_id <= len(TEST_CASES):
        print(
            f"Use 1 <= start-case-id <= end-case-id <= {len(TEST_CASES)}.",
            file=sys.stderr,
        )
        return 2

    requested = set(args.database_ids)
    invalid = requested.difference(DATABASE_ORDER)
    if invalid:
        print(
            f"Unsupported database IDs: {sorted(invalid)}. "
            f"Supported IDs: {DATABASE_ORDER}",
            file=sys.stderr,
        )
        return 2

    selected_db_ids = [db for db in DATABASE_ORDER if db in requested]
    selected_cases = [
        case
        for case in TEST_CASES
        if case["database_id"] in selected_db_ids
        and args.start_case_id <= case["id"] <= args.end_case_id
    ]
    if not selected_cases:
        print("No cases matched the requested range.", file=sys.stderr)
        return 2

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    output_dir = Path("benchmarks") / "results"
    output_dir.mkdir(parents=True, exist_ok=True)
    output_path = (
        Path(args.output)
        if args.output
        else output_dir / f"containment_500_four_databases_{timestamp}.txt"
    )
    output_path.parent.mkdir(parents=True, exist_ok=True)

    overall = collections.Counter()
    per_database = {
        database_id: collections.Counter()
        for database_id in selected_db_ids
    }
    per_difficulty = collections.defaultdict(collections.Counter)
    relationship_counts = collections.Counter()
    trace_runs: dict[int, str] = {}

    print("=" * 120)
    print("SpiderSQL — 500 Natural-Language Containment Cases")
    print(f"Database order: {selected_db_ids}")
    print(f"Selected cases: {len(selected_cases)}")
    print(f"One result file: {output_path.resolve()}")
    print("=" * 120)

    with output_path.open("w", encoding="utf-8") as report:
        report.write(
            "SpiderSQL 500-Case Natural-Language Containment Benchmark\n"
        )
        report.write(
            f"Started: {datetime.now().isoformat(timespec='seconds')}\n"
        )
        report.write(f"Base URL: {args.base_url}\n")
        report.write(f"Database order: {selected_db_ids}\n")
        report.write(f"Selected cases: {len(selected_cases)}\n")
        report.write(
            "Manual category labels are intentionally absent. "
            "Cases are classified only by database and difficulty.\n"
        )
        report.write(
            "Endpoint success means execution completed; generated SQL and "
            "containment relationships still require semantic review.\n\n"
        )

        overall_position = 0

        for database_id in selected_db_ids:
            database_cases = [
                case
                for case in selected_cases
                if case["database_id"] == database_id
            ]
            if not database_cases:
                continue

            database_name = DATABASE_NAMES[database_id]
            trace_run = f"{args.trace_prefix}_db{database_id}_{timestamp}"
            trace_runs[database_id] = trace_run

            print()
            print("#" * 120)
            print(
                f"STARTING DB{database_id} {database_name.upper()} — "
                f"{len(database_cases)} CASES"
            )
            print(f"Trace run: {trace_run}")
            print("#" * 120)

            report.write("\n" + "#" * 120 + "\n")
            report.write(
                f"START DATABASE DB{database_id} — "
                f"{database_name.upper()}\n"
            )
            report.write(f"Cases: {len(database_cases)}\n")
            report.write(f"Trace run: {trace_run}\n")
            report.write("#" * 120 + "\n")

            for database_position, case in enumerate(database_cases, start=1):
                overall_position += 1
                print()
                print("-" * 120)
                print(
                    f"[overall {overall_position:03d}/{len(selected_cases):03d}] "
                    f"[DB{database_id} "
                    f"{database_position:03d}/{len(database_cases):03d}] "
                    f"CASE {case['id']:03d} | {case['difficulty']} | "
                    f"{case['name']}"
                )
                for number, query in enumerate(case["queries"], start=1):
                    print(f"  Q{number}: {query}")

                status_code, payload, elapsed = post_batch(
                    args.base_url,
                    database_id,
                    case["queries"],
                    args.timeout,
                    case_id=case["id"],
                    difficulty=case["difficulty"],
                    trace_run=trace_run,
                )

                success = (
                    200 <= status_code < 300
                    and payload.get("success") is True
                )
                status_key = "success" if success else "failure"

                overall[status_key] += 1
                overall["queries_requested"] += len(case["queries"])
                per_database[database_id][status_key] += 1
                per_database[database_id]["queries_requested"] += len(
                    case["queries"]
                )
                per_difficulty[case["difficulty"]][status_key] += 1

                query_results = payload.get("query_results") or []
                pairs = payload.get("pairwise_relationships") or []
                overall["query_results_returned"] += len(query_results)
                overall["pairwise_results_returned"] += len(pairs)
